import type { AgentPlaygroundFeature } from "./types";

function feature(
  id: string,
  title: string,
  category: AgentPlaygroundFeature["category"],
  summary: string,
  description: string,
  capabilities: string[],
  workflowTitle: string,
  nodes: string[],
  engineeringNotes: string[],
): AgentPlaygroundFeature {
  return {
    id,
    title,
    category,
    summary,
    description,
    capabilities,
    workflow: { title: workflowTitle, nodes },
    engineeringNotes,
  };
}

